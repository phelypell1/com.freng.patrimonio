public class cadCidadeBeans {
    
    private int cid_codigo;
    private int est_codigo;
    private String cid_municipio;
    private int reg_codigo;

    /**
     * @return the cid_codigo
     */
    public int getCid_codigo() {
        return cid_codigo;
    }

    /**
     * @param cid_codigo the cid_codigo to set
     */
    public void setCid_codigo(int cid_codigo) {
        this.cid_codigo = cid_codigo;
    }

    /**
     * @return the est_codigo
     */
    public int getEst_codigo() {
        return est_codigo;
    }

    /**
     * @param est_codigo the est_codigo to set
     */
    public void setEst_codigo(int est_codigo) {
        this.est_codigo = est_codigo;
    }

    /**
     * @return the cid_municipio
     */
    public String getCid_municipio() {
        return cid_municipio;
    }

    /**
     * @param cid_municipio the cid_municipio to set
     */
    public void setCid_municipio(String cid_municipio) {
        this.cid_municipio = cid_municipio;
    }

    /**
     * @return the reg_codigo
     */
    public int getReg_codigo() {
        return reg_codigo;
    }

    /**
     * @param reg_codigo the reg_codigo to set
     */
    public void setReg_codigo(int reg_codigo) {
        this.reg_codigo = reg_codigo;
    }
}