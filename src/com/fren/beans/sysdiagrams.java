package com.fren.beans;

/**
 *
 * @author Phelype
 */
public class sysdiagrams {
        private String name; 
        private int principal_id; 
        private int diagram_id; 
        private int version; 
        private int definition;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the principal_id
     */
    public int getPrincipal_id() {
        return principal_id;
    }

    /**
     * @param principal_id the principal_id to set
     */
    public void setPrincipal_id(int principal_id) {
        this.principal_id = principal_id;
    }

    /**
     * @return the diagram_id
     */
    public int getDiagram_id() {
        return diagram_id;
    }

    /**
     * @param diagram_id the diagram_id to set
     */
    public void setDiagram_id(int diagram_id) {
        this.diagram_id = diagram_id;
    }

    /**
     * @return the version
     */
    public int getVersion() {
        return version;
    }

    /**
     * @param version the version to set
     */
    public void setVersion(int version) {
        this.version = version;
    }

    /**
     * @return the definition
     */
    public int getDefinition() {
        return definition;
    }

    /**
     * @param definition the definition to set
     */
    public void setDefinition(int definition) {
        this.definition = definition;
    }
        
        
    
}
